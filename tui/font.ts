export const GLYPH_HEIGHT = 7;
export const GLYPH_WIDTH = 10; // 5 dots × 2 columns each

/** Rows of block characters for a glyph, double-wide ("█████" style). */
export function renderGlyph(char: string): string[] {
  const glyph = GLYPHS[char.toUpperCase()];
  if (!glyph) return Array.from({ length: GLYPH_HEIGHT }, () => " ".repeat(GLYPH_WIDTH));
  return glyph.map((row) => row.split("").map((dot) => (dot === "#" ? "██" : "  ")).join(""));
}
