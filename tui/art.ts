const UNKNOWN = [
  "  ______",
  " |      |",
  " |  ??  |",
  " |      |",
  " |______|",
];

/** Rows for a card's drawing, padded to a uniform width. */
export function cardArt(word: string): string[] {
  const rows = CARD_ART[word] ?? UNKNOWN;
  const width = Math.max(...rows.map((r) => r.length));
  return rows.map((r) => r.padEnd(width, " "));
}
